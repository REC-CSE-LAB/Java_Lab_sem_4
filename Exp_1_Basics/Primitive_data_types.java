// Source Code :

package com.College_Java_Lab.Exp_1_Basics;

public class Primitive_data_types {
    public static void main(String[] args) {
        boolean b = false;
        char c = 0;
        short s = 0;
        int i = 0;
        long l = 0;
        float f = 0;
        double d = 0;
        System.out.println("Boolean value = " + b);
        System.out.println("Character value = " + c);
        System.out.println("Short/Short Integer value = " + s);
        System.out.println("Integer value = " + i);
        System.out.println("Long/Long Integer value = " + l);
        System.out.println("Floating point value = " + f);
        System.out.println("Double floating point value = " + d);
    }
}


// Output :
// Boolean value = false
// Character value = 0
// Short/Short Integer value = 0
// Integer value = 0
// Long/Long Integer value = 0
// Floating point value = 0.0
// Double floating point value = 0.0